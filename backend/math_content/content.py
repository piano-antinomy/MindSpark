def get_score_message(percentage):
    """Get an encouraging message based on quiz score"""
    import random
    
    if percentage >= 90:
        return random.choice(score_messages["perfect"])
    elif percentage >= 75:
        return random.choice(score_messages["excellent"])
    elif percentage >= 60:
        return random.choice(score_messages["good"])
    else:
        return random.choice(score_messages["needs_practice"])

